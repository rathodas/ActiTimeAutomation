package com.technocrats.core.Entity;

public class TestScriptStepGenerator {
	private String Reference;
	private String AutomationScriptName;
	private String StepKeyword;
	private String StepGroup;
	private String SkipStep;
	
	public String getReference() {
		return Reference;
	}
	public void setReference(String reference) {
		Reference = reference;
	}
	public String getAutomationScriptName() {
		return AutomationScriptName;
	}
	public void setAutomationScriptName(String automationScriptName) {
		AutomationScriptName = automationScriptName;
	}
	public String getStepKeyword() {
		return StepKeyword;
	}
	public void setStepKeyword(String stepKeyword) {
		StepKeyword = stepKeyword;
	}
	public String getStepGroup() {
		return StepGroup;
	}
	public void setStepGroup(String stepGroup) {
		StepGroup = stepGroup;
	}
	public String getSkipStep() {
		return SkipStep;
	}
	public void setSkipStep(String skipStep) {
		SkipStep = skipStep;
	}


}
